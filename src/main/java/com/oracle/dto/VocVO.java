package com.oracle.dto;

public class VocVO {
	private String uvid;
	private String uv_usid;
	private String voc;
	private String answer;
	
	public String getUvid() {
		return uvid;
	}
	public void setUvid(String uvid) {
		this.uvid = uvid;
	}
	public String getUv_usid() {
		return uv_usid;
	}
	public void setUv_usid(String uv_usid) {
		this.uv_usid = uv_usid;
	}
	public String getVoc() {
		return voc;
	}
	public void setVoc(String voc) {
		this.voc = voc;
	}
	public String getAnswer() {
		return answer;
	}
	public void setAnswer(String answer) {
		this.answer = answer;
	}
}
